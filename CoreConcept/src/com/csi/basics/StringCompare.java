package com.csi.basics;

public class StringCompare {
	public static void main(String[] args) {
		String s1="orange";
		String s2="apple";
		String s3="banana";
		System.out.println(s1.compareTo(s2));//positive value it compare in the terms of alphabetical order and char number
		System.out.println(s2.compareTo(s3));
	}

}
